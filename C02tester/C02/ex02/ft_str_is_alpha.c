#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int		i;
	char	x;

	i = 0;
	while (str[i])
	{
		x = str[i++];
		if ((x < 'a' || x > 'z') && (x < 'A' || x > 'Z'))
			return (0);
	}
	return (1);
}
